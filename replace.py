
import shutil

# 指定原始檔案和目標檔案名稱
source_file = 'C:/Users/eric/Desktop/aff_folder/hsiav.github.io/index.md'
target_file = 'C:/Users/eric/Desktop/aff_folder/hsiav.github.io/Readme.md'

# 使用 shutil.copy2 來複製檔案，並同時保留原始檔案的屬性和時間戳
shutil.copy2(source_file, target_file)

print(f'檔案 {source_file} 已複製為 {target_file}')



